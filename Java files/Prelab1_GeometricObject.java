package seven;

public class Prelab1_GeometricObject 
{
	private String border_color;
	private String fill_color;
	private double border_width;
	private boolean fill;
	public String getBorder_color() 
	{
		return border_color;
	}
	public void setBorder_color(String border_color) 
	{
		this.border_color = border_color;
	}
	public String getFill_color() 
	{
		return fill_color;
	}
	public void setFill_color(String fill_color) 
	{
		this.fill_color = fill_color;
	}
	public double getBorder_width() 
	{
		return border_width;
	}
	public void setBorder_width(double border_width) 
	{
		this.border_width = border_width;
	}
	public boolean isFill() 
	{
		return fill;
	}
	public void setFill(boolean fill) 
	{
		this.fill = fill;
	}
	
}
